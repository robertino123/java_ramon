package exercitii;

public class Numar {
	int numar_intreg = 1;
	
	public int suma(int a) {
		int sum = a + numar_intreg;
		return sum;
	}
	
	public int suma(int a, int b) {
		int sum = a + b + numar_intreg;
		return sum;
	}
	
	public int suma(int a, int b, int c) {
		int sum = a + b + c +  numar_intreg;
		return sum;
	}
	
	public int suma(int a, int b, int c, int d) {
		int sum = a + b + c + d +  numar_intreg;
		return sum;
	}
}
